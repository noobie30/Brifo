#!/usr/bin/env node
/*
 * Generates api/_backend-manifest.js with a static require() for every .js
 * file under api/_backend/. This exists because Vercel's Node File Tracer
 * ships only the files it can statically reach from api/index.js at trace
 * time. Despite `includeFiles: "api/_backend/**\/*"` in vercel.json, in
 * practice some transitive requires (e.g. auth.controller → ./auth.service)
 * were dropped and the Lambda started with a `Cannot find module
 * './auth.service'` crash. Explicit top-level requires are the one thing
 * NFT is guaranteed to follow, so we generate them for every compiled file.
 *
 * This runs as part of `vercel-build` AFTER backend/dist is copied into
 * api/_backend/. The output file is committed to git so local `tsc`-only
 * runs still see it (Vercel's build will overwrite it to keep it in sync).
 */
const fs = require("node:fs");
const path = require("node:path");

const ROOT = path.resolve(__dirname, "..");
const BACKEND_DIR = path.join(ROOT, "api", "_backend");
const OUTPUT_FILE = path.join(ROOT, "api", "_backend-manifest.js");

function walkJs(dir) {
  const results = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      results.push(...walkJs(full));
      continue;
    }
    if (!entry.isFile()) continue;
    if (!entry.name.endsWith(".js")) continue;
    if (entry.name.endsWith(".map.js")) continue; // source maps
    results.push(full);
  }
  return results.sort();
}

if (!fs.existsSync(BACKEND_DIR)) {
  console.error(
    `[generate-api-manifest] ${BACKEND_DIR} does not exist. Run backend build + copy step first.`,
  );
  process.exit(1);
}

const files = walkJs(BACKEND_DIR);
if (files.length === 0) {
  console.error(`[generate-api-manifest] No .js files found under ${BACKEND_DIR}`);
  process.exit(1);
}

const requires = files
  .map((abs) => {
    // Relative path from api/ (where the manifest lives) without the .js ext.
    // Using POSIX separators so the emitted require() string works on any OS.
    const rel = path
      .relative(path.join(ROOT, "api"), abs)
      .split(path.sep)
      .join("/");
    const relNoExt = rel.replace(/\.js$/, "");
    return `require("./${relNoExt}");`;
  })
  .join("\n");

const header = `// AUTO-GENERATED by scripts/generate-api-manifest.js — DO NOT EDIT BY HAND.
// Forces Vercel's Node File Tracer to bundle every compiled backend module
// into the Lambda. Re-generated on every \`vercel-build\` run.
// File count: ${files.length}
/* eslint-disable */
`;

fs.writeFileSync(OUTPUT_FILE, `${header}\n${requires}\n`);

// eslint-disable-next-line no-console
console.log(
  `[generate-api-manifest] wrote ${OUTPUT_FILE} with ${files.length} requires.`,
);
